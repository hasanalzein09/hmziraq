// توليد كلمات بحث مخصصة لكل مدينة
export function generateCityKeywords(
  businessType: string,
  city: 'baghdad' | 'basra' | 'najaf' | 'karbala'
): string[] {
  const cityData = MAIN_IRAQI_CITIES[city];
  const keywords: string[] = [];

  // إضافة اسم المدينة
  keywords.push(`${businessType} ${cityData.ar}`);
  keywords.push(`${businessType} ب${cityData.ar}`);
  keywords.push(`احسن ${businessType} ب${cityData.ar}`);
  keywords.push(`ارخص ${businessType} ب${cityData.ar}`);
  keywords.push(`وين الكه ${businessType} ب${cityData.ar}`);

  // إضافة الأحياء
  cityData.districts.slice(0, 10).forEach(district => {
    keywords.push(`${businessType} ${district}`);
    keywords.push(`${businessType} ب${district}`);
    keywords.push(`وين الكه ${businessType} ب${district}`);
  });

  return keywords;
}

